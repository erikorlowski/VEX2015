#include "main.h"

/**
 * Returns the output from the joystick for the left side of the drive.
 */
int OIGetDriveLeft()
{
	return joystickGetAnalog(1,3);
}

/**
 * Returns the output from the joystick for the right side of the drive.
 */
int OIGetDriveRight()
{
	return joystickGetAnalog(1,2);
}
