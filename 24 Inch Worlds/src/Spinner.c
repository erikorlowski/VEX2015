#include "main.h"

Spinner initSpinner(int servo)
{
	Spinner newSpinner = {servo};
	return newSpinner;
}

void spinnerUp(Spinner spinner)
{
	motorSet(spinner.servo, 127);
}

void spinnerDown(Spinner spinner)
{
	motorSet(spinner.servo, 127);
}
